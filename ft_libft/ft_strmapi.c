#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	int		i;
	char	*str;

	i = 0;
	str = ft_calloc (ft_strlen(s) + 1, sizeof(char));
	if (str == NULL || s == 0)
		return (NULL);
	while (s[i] != '\0')
	{
		str[i] = f(i, s[i]);
		i++;
	}
	str[i] = '\0';
	return (str);
}

/*ritorna una stringa uguale ad s dopo l'applicazione della funzione f*/
